package loop;
// Example to print PINNum 
import java.util.Scanner;

public class ScannerEx2 {

	public static void main(String[] args) {
		int PINNum;
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter the PINNum : ");
		PINNum=sc.nextInt();
		System.out.println("Your PIN Num : " +PINNum);

	}

}
